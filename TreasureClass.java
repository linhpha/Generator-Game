public class TreasureClass {
	
	public String treasureClass;
	public String[] itemArray;

	/**
	 * Constructor for the TreasureClass Object
	 * @param treasureClass the name of the TreasureClass Item
	 * @param itemArray an array of the three items associated with this TreasureClass
	 */
	public TreasureClass(String treasureClass, String[] itemArray) {
		this.treasureClass = treasureClass;
		this.itemArray = itemArray; 

	}	
	
}
